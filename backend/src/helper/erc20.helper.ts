import { Provider } from '@ethersproject/providers';
import { getContract } from './contract.helper';

export const getTokenProperty = async (provider: Provider, tokenAddress: string) => {
  const contract = await getContract(provider, tokenAddress, ERC20_ABI);
  const [name, symbol, decimals, supply] = await Promise.all([
    contract.name(),
    contract.symbol(),
    contract.decimals(),
    contract.totalSupply(),
  ]);
  return { name, symbol, decimals, supply };
};

export const getTokenTotalSupply = async (provider: Provider, tokenAddress: string) => {
  return (await getContract(provider, tokenAddress, ERC20_ABI)).totalSupply();
};

export const getTokenBalance = async (provider: Provider, tokenAddress: string, userAddress: string) => {
  return (await getContract(provider, tokenAddress, ERC20_ABI)).balanceOf(userAddress);
};

export const getTokenName = async (provider: Provider, tokenAddress: string) => {
  return (await getContract(provider, tokenAddress, ERC20_ABI)).name();
};

export const getTokenSymbol = async (provider: Provider, tokenAddress: string) => {
  return (await getContract(provider, tokenAddress, ERC20_ABI)).symbol();
};

export const getTokenDecimals = async (provider: Provider, tokenAddress: string) => {
  return (await getContract(provider, tokenAddress, ERC20_ABI)).decimals();
};

